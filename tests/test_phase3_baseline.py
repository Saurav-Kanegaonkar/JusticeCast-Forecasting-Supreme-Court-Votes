def test_baseline_results_metrics_in_valid_ranges(baseline_results):
    for col in ["accuracy", "balanced_accuracy", "precision", "recall", "f1", "roc_auc"]:
        assert (baseline_results[col] >= 0).all() and (baseline_results[col] <= 1).all(), (
            f"{col} out of [0,1]"
        )


def test_no_combo_is_anti_predictive(baseline_results):
    """The right mechanical-correctness check: ROC AUC must be >= 0.5 (random).

    Note: raw accuracy below the 62.4% global majority baseline is EXPECTED
    behavior for `class_weight='balanced'` linear models — they trade
    majority-class accuracy for class-balanced predictions. The cai-plan's
    "below baseline = bug" check applies to unweighted models; we use
    class_weight='balanced' per Non-Negotiable #6."""
    bad = baseline_results[baseline_results["roc_auc"] < 0.5]
    assert bad.empty, (
        f"Combos with ROC AUC < 0.5 (anti-predictive): "
        f"{bad[['combo_id', 'roc_auc']].to_dict('records')}"
    )


def test_per_justice_lift_includes_all_16_justices():
    if not p3.PER_JUSTICE_LIFT_PATH.exists():
        pytest.skip("per_justice_lift.csv missing")
    lift = pd.read_csv(p3.PER_JUSTICE_LIFT_PATH)
    # 16 Justices × 9 combos = 144 rows expected (some Justices may have 0 test rows)
    n_justices = lift["oyez_identifier"].nunique()
    assert n_justices >= 14, (
        f"Expected ≥14 of 16 Justices in per-Justice lift table; got {n_justices}"
    )


def test_top_features_csv_has_60_rows():
    if not p3.TOP_FEATURES_PATH.exists():
        pytest.skip("top_features_best_linear.csv missing")
    feats = pd.read_csv(p3.TOP_FEATURES_PATH)
