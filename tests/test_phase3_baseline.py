# ---------- Pipeline construction ----------

def test_all_three_vectorizers_use_custom_stopwords():
    """Non-Negotiable #3: all three vectorizers consume STOPWORDS_FOR_VECTORIZER."""
    vecs = p3.make_vectorizers()
    assert set(vecs) == {"bow_unigram", "tfidf_unigram", "tfidf_bigram"}
    for name, v in vecs.items():
        assert v.stop_words is STOPWORDS_FOR_VECTORIZER, (
            f"{name} doesn't pass STOPWORDS_FOR_VECTORIZER as stop_words"
        )


def test_classifier_set_is_logreg_svc_rf():
    clfs = p3.make_classifiers()
    assert set(clfs) == {"logreg", "linear_svc", "random_forest"}


def test_is_linear_combo():
    assert p3.is_linear_combo("logreg")
    assert p3.is_linear_combo("linear_svc")
    assert not p3.is_linear_combo("random_forest")


# ---------- Split mechanics (Non-Negotiable #1) ----------

def test_stratified_group_split_has_no_case_leakage():
    """Train and test must not share any caseId."""
    df = pd.read_parquet(p3.MODELING_TABLE_PATH).reset_index(drop=True)
    y = df["voted_petitioner"].astype(int).values
    groups = df["caseId"].values

    sgkf = StratifiedGroupKFold(n_splits=5, shuffle=True, random_state=p3.RANDOM_STATE)
    splits = list(sgkf.split(df["text"], y, groups=groups))
    train_idx, test_idx = splits[0][0], splits[0][1]

    train_cases = set(df.iloc[train_idx]["caseId"])
    test_cases = set(df.iloc[test_idx]["caseId"])
    assert not (train_cases & test_cases), (
        f"Case-id leakage: {len(train_cases & test_cases)} overlapping caseIds"
    )


def test_random_state_is_42():
    """Reproducibility: Non-Negotiable #5."""
    assert p3.RANDOM_STATE == 42


