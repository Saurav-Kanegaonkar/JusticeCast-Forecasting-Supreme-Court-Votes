# ---------- Configuration constants (no compute) ----------

def test_bow_best_params_match_phase4_winner():
    """Sanity: the hard-coded BoW best params match what phase4_test_eval.csv
    reported as the winner. If Phase 4 is re-run with different results,
    update Phase 5 too."""
    p4 = pd.read_csv(R / "phase4_test_eval.csv")
    winner = p4.loc[p4["test_roc_auc"].idxmax()]
    assert winner["model"] == "linear_svc", (
        f"Phase 4 winner is {winner['model']!r}, but phase5_evaluation expects 'linear_svc'"
    )


