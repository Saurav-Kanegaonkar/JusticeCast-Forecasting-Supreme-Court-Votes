def test_triad_has_three_slices_per_track(triad):
    pivot = triad.groupby(["track", "slice"]).size().unstack(fill_value=0)
    assert set(pivot.columns) == {"global", "unanimous", "contested"}
    assert set(pivot.index) == {"bow", "embeddings"}


def test_triad_global_matches_existing_per_justice_csvs(triad):
    """The global slice in the triad should match the per-Justice AUCs already
    produced in Phase 4 / 4.5 (within rounding)."""
    bow_global = triad[(triad.track == "bow") & (triad.slice == "global")][
        ["oyez_identifier", "point_auc"]
    ].set_index("oyez_identifier")["point_auc"]
    bow_p4 = pd.read_csv(R / "phase4_per_justice_auc.csv")[
        ["oyez_identifier", "point_auc"]
    ].set_index("oyez_identifier")["point_auc"]
    common = bow_global.index.intersection(bow_p4.index)
    diff = (bow_global[common] - bow_p4[common]).abs()
    # Allow tiny numerical drift; should be near-zero
    assert diff.dropna().max() < 0.001, (
        f"Global per-Justice AUC differs from Phase 4 csv by {diff.max():.4f}"
    )


def test_contested_lift_is_meaningful(triad):
    """The contested-cases slice is the deck's headline. Embeddings should
    have higher mean per-Justice AUC than BoW on contested cases."""
    contested = triad[(triad.slice == "contested") & triad.point_auc.notna()]
    bow_mean = contested[contested.track == "bow"]["point_auc"].mean()
    emb_mean = contested[contested.track == "embeddings"]["point_auc"].mean()
    assert emb_mean > bow_mean, (
        f"Embeddings ({emb_mean:.3f}) should beat BoW ({bow_mean:.3f}) on contested"
    )
    # The headline gap is ~0.04 — guard against accidental regressions
    assert emb_mean - bow_mean > 0.02, (
        f"Lift on contested ({emb_mean - bow_mean:.3f}) seems too small; "
        f"check the refit + scoring path"
    )


